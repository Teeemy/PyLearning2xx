# Lambda expression used for converting any function to one line
# to save the length of a file

pow_of_two = lambda num: num ** 2  # using lambda to calculate the power of 2
print(pow_of_two(2))

d_salary = lambda salary: salary * 2  # using lambda to double a num
print(d_salary(10))

m_wages = lambda wages: wages * 3
print(m_wages(4))

sum = lambda a, b: a + b  # creating a sum function using lambda expression
print(sum(3, 4))

say_my_name = lambda name: print("Your name is ",name) # using lambda to print name.it is really used
say_my_name("Ashabi")

